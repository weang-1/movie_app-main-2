class Movie {
  final String id;
  final String title;
  final String year;
  final String genre;
  final String director;
  final String description;
  final String posterUrl;
  final double rating;
  final int duration;

  Movie({
    required this.id,
    required this.title,
    required this.year,
    required this.genre,
    required this.director,
    required this.description,
    required this.posterUrl,
    required this.rating,
    required this.duration,
  });
}
